var currentLanguage = 'en';

function translatePage(lang) {
    currentLanguage = lang;
    document.querySelectorAll('[data-translate]').forEach(el => {
        const key = el.dataset.translate;
        if (translation[lang] && translation[lang][key]) {
            el.textContent = translation[lang][key];
        }
    });
    document.querySelectorAll('[data-translate-placeholder]').forEach(el => {
        const key = el.dataset.translatePlaceholder;
        if (translation[lang] && translation[lang][key]) {
            el.setAttribute('placeholder', translation[lang][key]);
        }
    });
    document.documentElement.lang = lang === 'fr' ? 'fr' : 'en';

    // Sync both lang switchers
    document.querySelectorAll('.lang-switcher button, .lang-switcher-mob button').forEach(btn => {
        const btnLang = btn.textContent.trim();
        if (btnLang === lang.toUpperCase() || (lang === 'en' && btnLang === 'EN') || (lang === 'fr' && btnLang === 'FR')) {
            btn.classList.remove('inactive');
        } else {
            btn.classList.add('inactive');
        }
    });
}

document.addEventListener('DOMContentLoaded', function () {
    translatePage(currentLanguage);

    document.querySelectorAll('.lang-switcher button, .lang-switcher-mob button').forEach(btn => {
        btn.addEventListener('click', function () {
            const lang = this.textContent.trim() === 'FR' ? 'fr' : 'en';
            translatePage(lang);
        });
    });
});
